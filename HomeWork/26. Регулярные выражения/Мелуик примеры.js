alert("вот досада же!".match(/(\b|\s|^|$)[а-я]{3}\1/g));
/* Тут вспоминаем, что такое диапазоны ([а-я]=1 символ в этом диапазоне) и обратные ссылки.
Тут сказано: ищем слово, перед ним пробел, начало строки или конец строки, затем 3 русские строчные
буквы, а затем обратная ссылка на первую скобочную группу => после него пробел, начало строки, или
конец строки */

alert("Вот досада же!".match(/(\b|\s|^|$)[А-Яа-я]{3}\1/g));
// Диапазоны указываются один за другим, без лишних знаков или пробелов.